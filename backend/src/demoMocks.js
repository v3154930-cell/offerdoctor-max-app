/**
 * Get mock response for given scenario, platform, and mode
 * @param {string} scenario - marketplace | avito | landing
 * @param {string} platform - ozon | wb | ym (only for marketplace)
 * @param {string} mode - preview | full
 * @param {string} tariff - main | competitor (only for full)
 * @returns {Object}
 */
function getMockResponse(scenario, platform, mode, tariff) {
    var data;

    if (scenario === 'marketplace') {
        var p = platform || 'ozon';
        data = mockData.marketplace[p] || mockData.marketplace.ozon;
    } else {
        data = mockData[scenario] || mockData.avito;
    }

    if (mode === 'preview') {
        return data.preview;
    }

    var result = Object.assign({}, data.full);

    if (tariff !== 'competitor') {
        delete result.competitorAnalysis;
    }

    return result;
}

module.exports = {
    getMockResponse: getMockResponse,
    mockData: mockData
};
